DragWidget::DragWidget(QWidget *parent)
    : QFrame(parent)
{
    setMinimumSize(200, 200);
    setFrameStyle(QFrame::Sunken | QFrame::StyledPanel);

    JointTypes jointType = fixed;
    Joint *fixedSupportIcon = new Joint(this, jointType);
    fixedSupportIcon->move(10, 10);

    jointType = linear;
    Joint *linSupportIcon = new Joint(this, jointType);
    linSupportIcon->move(10, 100);

    jointType = guided;
    Joint *varSupportIcon = new Joint(this, jointType);
    varSupportIcon->move(10, 200);

}
